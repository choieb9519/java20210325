package p04.lecture;

public class A08Switch {
	public static void main(String[] args) {
		//switch case: 값이 일치하는 case문 부터 실행
		
		int a = 1;
		
		switch (a) {
		case 1:
			System.out.println("케이스 1");
			break;
		case 2:
			System.out.println("케이스 2");
			break;
		case 3:
			System.out.println("케이스 3");
			
		}
	}
}
