package p03.lecture.p2binary.p2compare;

public class A01Compare {
	public static void main(String[] args) {
		// ==, !=, <, <=, >, >=
		//비교연산자의 결과는 boolean type
		
		int a = 3;
		int b = 8;
		
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a > b);
		System.out.println(a >= b);
		System.out.println(a < b);
		System.out.println(a <= b);
		
	}
}
